class AssignmentOperators {
    public static void main(String[] args) {
        int a = 5;
        System.out.println("Original value: " + a);

        a += 3;
        System.out.println("a += 3: " + a+"\n");

        a -= 2;
        System.out.println("a -= 2: " + a+"\n");

        a *= 4;
        System.out.println("a *= 4: " + a+"\n");

        a /= 6;
        System.out.println("a /= 6: " + a+"\n");

        a %= 3;
        System.out.println("a %= 3: " + a+"\n");
    }
}